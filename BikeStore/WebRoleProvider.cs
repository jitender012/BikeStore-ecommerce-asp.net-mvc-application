        public override string[] GetRolesForUser(string Email)
        {

            using(var context = new BikeStores_2Entities())
            {
                var result = (from user in context.customers
                              join role in context.UserRoles on user.customer_id equals role.customer_id
                              where user.email == Email
                              select role.role).ToArray();
                return result;
            }
